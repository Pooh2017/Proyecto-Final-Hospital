    private void generarPDF(String Nombre, String RFC, String fecha, String condicion, String notas)throws FileNotFoundException,DocumentException{
        Document document = new Document();
        PdfWriter.getInstance(document, new FileOutputStream("SalidaPaciente.pdf"));
        document.open();
        document.add(new Paragraph("Informaci√≥n de salida del paciente"));
        document.add(new Paragraph("Nombre: "+ Nombre));
        document.add(new Paragraph("RFC: "+RFC));
        document.add(new Paragraph("Fecha: "+fecha));
        document.add(new Paragraph("Condici√≥n: "+condicion));
        document.add(new Paragraph("Notas Medicas: "+notas));
        document.close();
    }

