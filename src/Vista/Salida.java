    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String nombre = txtNombre.getText();
        String rfc = txtRFC.getText();
        java.util.Date fecha = txtFecha.getDate();
        String condicion = txtCondicion.getText();
        String notas = txtNotas.getText();

        Connection con = Conexion.conectar();

        try {
            String sqlEliminar = "DELETE FROM internar WHERE rfc = ?";
            PreparedStatement psEliminar = con.prepareStatement(sqlEliminar);
            psEliminar.setString(1, rfc);
            psEliminar.executeUpdate();

            String sqlInsertar = "INSERT INTO historial (nombre_paciente, rfc, descripcion, fecha, motivo, area, numero_piso, numero_cuarto, tratamiento) "
                    + "VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?)";
            PreparedStatement psInsertar = con.prepareStatement(sqlInsertar);
            psInsertar.setString(1, nombre);
            psInsertar.setString(2, rfc);
            psInsertar.setString(3, "Dado de alta");
            psInsertar.setDate(4, new java.sql.Date(fecha.getTime()));
            psInsertar.setString(5, "");
            psInsertar.setString(6, ""); 
            psInsertar.setString(7, "");
            psInsertar.setString(8, ""); 
            psInsertar.setString(9, "");
            psInsertar.executeUpdate();

            con.close();
            JOptionPane.showMessageDialog(this, "Paciente dado de alta y registrado en el historial.");
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error al actualizar la base de datos: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

