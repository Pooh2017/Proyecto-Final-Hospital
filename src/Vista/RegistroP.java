    private void btnGuardarRActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarRActionPerformed

        // TODO add your handling code here:
        Paciente paciente = new Paciente();
        Ctlr_Paciente controlPaciente = new Ctlr_Paciente();
        String metodoLlegada = cboLlegada.getSelectedItem().toString();
        if (!controlPaciente.existePaciente(txtRFC.getText().trim())) {
            paciente.setNombre(txtNombre.getText().trim());
            paciente.setApellidoP(txtApellidoP.getText().trim());
            paciente.setApellidoM(txtApellidoM.getText().trim());
            SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
            Date date = txtFechaNacimiento.getDate();
            String fechaNacimiento = dateFormat.format(date);
            paciente.setFechaNacimiento(fechaNacimiento);

            if (btnM.isSelected()) {
                paciente.setSexo("M");
            } else if (btnF.isSelected()) {
                paciente.setSexo("F");
            }
            paciente.setDireccion(txtDireccion.getText().trim());
            paciente.setCURP(txtCURP.getText().trim());
            paciente.setRFC(txtRFC.getText().trim());
            paciente.setTelefono(txtTelefono.getText().trim());
            paciente.setMotivo(txtMotivo.getText().trim());
            paciente.setModoLlegada(metodoLlegada);

            Date fechaIngresoDate = txtFechaIngreso.getDate();
            String fechaIngreso = dateFormat.format(fechaIngresoDate);
            paciente.setFechaIngreso(fechaIngreso);

            if (controlPaciente.guardar(paciente)) {
                JOptionPane.showMessageDialog(null, "Paciente guardado con √©xito.");
            } else {
                JOptionPane.showMessageDialog(null, "Error al guardar el paciente.");
            }
        } else {
            JOptionPane.showMessageDialog(null, "El paciente ya se encuentra registrado.");
        }
//Falta agregar restriccion para que no hayan campos vacios.
    }//GEN-LAST:event_btnGuardarRActionPerformed

