    private void cargarTabla() {
        Connection con = Conexion.conectar();
        DefaultTableModel model = new DefaultTableModel();
        String sql = "SELECT nombre_paciente, rfc, fecha, motivo, area, numero_piso, numero_cuarto, tratamiento "
                + "FROM internar LIMIT ? OFFSET ?";

        PreparedStatement ps;

        try {
            ps = con.prepareStatement(sql);

            int limit = 20;
            int offset = 0;

            ps.setInt(1, limit);
            ps.setInt(2, offset);

            ResultSet rs = ps.executeQuery();

            model.setColumnIdentifiers(new String[]{
                "Nombre", "RFC", "Fecha que se internÃ³", "Diagnostico", "Ãrea", "NÃºmero de piso", "NÃºmero de cuarto", "Tratamiento"
            });

            while (rs.next()) {
                Object[] fila = new Object[8];
                fila[0] = rs.getString("nombre_paciente");
                fila[1] = rs.getString("rfc");
                fila[2] = rs.getDate("fecha");
                fila[3] = rs.getString("motivo");
                fila[4] = rs.getString("area");
                fila[5] = rs.getString("numero_piso");
                fila[6] = rs.getString("numero_cuarto");
                fila[7] = rs.getString("tratamiento");
                model.addRow(fila);
            }

            tblInternar.setModel(model);
            con.close();
        } catch (SQLException e) {
            System.out.println("Error al rellenar la tabla Pacientes internados: " + e);
        }
    }

    private void buscarPaciente(String criterio) {
        Connection con = Conexion.conectar();
        DefaultTableModel model = new DefaultTableModel();

        int inicio = (paginaActual - 1) * registrosPorPagina;

        String sql = "SELECT nombre_paciente, rfc, fecha, motivo, area, numero_piso, numero_cuarto, tratamiento "
                + "FROM internar WHERE nombre_paciente LIKE ? LIMIT ? OFFSET ?";

        PreparedStatement ps;

        try {
            ps = con.prepareStatement(sql);
            ps.setString(1, "%" + criterio + "%");
            ps.setInt(2, registrosPorPagina);
            ps.setInt(3, inicio);
            ResultSet rs = ps.executeQuery();

            model.setColumnIdentifiers(new String[]{
                "Nombre", "rfc", "Fecha que se internÃ³", "Diagnostico", "Ãrea", "NÃºmero de piso", "NÃºmero de cuarto", "Tratamiento"
            });

            while (rs.next()) {
                Object[] fila = new Object[8];
                fila[0] = rs.getString("nombre_paciente");
                fila[1] = rs.getString("rfc");
                fila[2] = rs.getDate("fecha");
                fila[3] = rs.getString("motivo");
                fila[4] = rs.getString("area");
                fila[5] = rs.getString("numero_piso");
                fila[6] = rs.getString("numero_cuarto");
                fila[7] = rs.getString("tratamiento");
                model.addRow(fila);
            }

            tblInternar.setModel(model);
            con.close();
        } catch (SQLException e) {
            System.out.println("Error al buscar paciente: " + e);
        }
    }
    
