    public void agregarRegistroHistorial(String nombreCompleto, String rfc, String motivo, Date fecha, String descripcion, String area, String piso, String cuarto, String tratamiento) {
    Connection con = Conexion.conectar();
    String sql = "INSERT INTO historial (nombre_paciente, rfc, motivo, fecha, descripcion, area, numero_piso, numero_cuarto, tratamiento) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?)";

    try {
        PreparedStatement ps = con.prepareStatement(sql);
        ps.setString(1, nombreCompleto);
        ps.setString(2, rfc);
        ps.setString(3, motivo);
        ps.setDate(4, new java.sql.Date(fecha.getTime()));
        ps.setString(5, descripcion);
        ps.setString(6, area);
        ps.setString(7, piso);
        ps.setString(8, cuarto);
        ps.setString(9, tratamiento);

        ps.executeUpdate();
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Error al agregar registro al historial: " + e.getMessage());
    } finally {
        try {
            con.close();
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }
}
