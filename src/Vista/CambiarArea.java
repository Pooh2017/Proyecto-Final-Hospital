    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        String nombreCompleto = txtNombre.getText();
        String rfc = txtRFC.getText();
        String diagnostico = txtMotivo.getText();
        String area = (String) cboArea.getSelectedItem();
        String piso = (String) cboPiso.getSelectedItem();
        String cuarto = (String) cboCuarto.getSelectedItem();
        String tratamiento = txtTratamiento.getText();

        // Actualizar en la base de datos
        Connection con = Conexion.conectar();
        String sql = "UPDATE internar SET motivo = ?, area = ?, numero_piso = ?, numero_cuarto = ?, tratamiento = ? WHERE rfc = ?";

        try {
            PreparedStatement ps = con.prepareStatement(sql);
            ps.setString(1, diagnostico);
            ps.setString(2, area);
            ps.setString(3, piso);
            ps.setString(4, cuarto);
            ps.setString(5, tratamiento);
            ps.setString(6, rfc);

            int filasActualizadas = ps.executeUpdate();
            if (filasActualizadas > 0) {
                JOptionPane.showMessageDialog(this, "Datos actualizados correctamente.");
                agregarRegistroHistorial(nombreCompleto, rfc, diagnostico, new Date(), "Cambio de área", area, piso, cuarto, tratamiento);
                this.dispose();
                verInternados.actualizarTabla(); // Actualizar la tabla en verInternados
            } else {
                JOptionPane.showMessageDialog(this, "No se pudo actualizar el registro.");
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error al actualizar datos: " + e.getMessage());
        } finally {
            try {
                con.close();
            } catch (SQLException ex) {
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

